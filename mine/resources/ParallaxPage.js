var ParallaxPage = function (opts) {
    this.options = $.extend({}, {
        pageSize: 200,
        startPage: 1,
        pages: [],
        loading: 'loading.gif'
    }, opts);

    if (!this.options.pages.length) {
        return;
    }

    this.init();
};

ParallaxPage.prototype = {
    constructor: ParallaxPage,
    init: function () {
        this.nowPage = this.options.startPage || 1;
        this.pages = this.options.pages;
    },
    start: function () {
        this.gotoPage(this.nowPage);
    },
    gotoPage: function (page) {
        this.showLoading();
        this._lazyImages(page).done(function () {
            this.playAnimate(page);
            this.hideLoading();
        }.bind(this));
    },
    _lazyImages: function (page) {
        var pages = this.pages[page - 1],
            count = pages.length,
            defer = new $.Deferred(),
            img = (function (img) {
                img.onload = function () {
                    if (count === 0) {
                        defer.resolve();
                    } else {
                        img.src = pages[count--].src;
                    }
                };
            }(new Image()));
        defer.done(function () {
            img = null;
        });
        img.src = pages[count--].src;
        return defer;
    },
    showLoading: function () {
    },
    hideLoading: function () {
    },
    playAnimate: function () {

    }
};
